from __future__ import annotations

from dataclasses import dataclass
from datetime import date


@dataclass(frozen=True)
class Quarter:
    year: int
    q: int  # 1..4

    @classmethod
    def from_date(cls, d: date) -> "Quarter":
        return cls(d.year, (d.month - 1) // 3 + 1)

    def label(self) -> str:
        return f"{self.year}년 {self.q}분기"

    def short(self) -> str:
        return f"{self.year}Q{self.q}"

    def start_date(self) -> date:
        return date(self.year, (self.q - 1) * 3 + 1, 1)

    def __lt__(self, other: "Quarter") -> bool:
        return (self.year, self.q) < (other.year, other.q)


