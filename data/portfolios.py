def list_quarters() -> list[Quarter]:
    return sorted(Quarter(y, q) for (y, q) in QUARTERLY_MODELS)


def current_quarter(today: date | None = None) -> Quarter:
    """오늘 날짜에 가장 가까운(이전·동일) 분기를 반환합니다."""
    today = today or date.today()
    target = Quarter.from_date(today)
    candidates = list_quarters()
    if not candidates:
        raise RuntimeError("QUARTERLY_MODELS 가 비어 있습니다.")
    eligible = [q for q in candidates if not (target < q)]
    return eligible[-1] if eligible else candidates[0]


def get_model(quarter: Quarter, profile: str) -> dict[str, float]:
    if profile not in RISK_PROFILES:
        raise KeyError(f"알 수 없는 리스크 모델: {profile}")
    return dict(QUARTERLY_MODELS[(quarter.year, quarter.q)][profile])


def get_note(quarter: Quarter) -> str:
    return NOTES.get((quarter.year, quarter.q), "")


def validate_all() -> list[str]:
    """모든 분기/모델의 비중 합계 = 1.0 검증. 위반 항목 리스트 반환."""
    errors: list[str] = []
    for (y, q), models in QUARTERLY_MODELS.items():
        for profile, w in models.items():
            s = sum(w.values())
            if abs(s - 1.0) > 1e-6:
                errors.append(f"{y}Q{q} · {profile}: 합계 {s:.4f}")
    return errors
